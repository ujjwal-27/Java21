public class WhileLoop {
    public static void main(String[] args) {
        int num = 1;

        while (num <= 5) {
            int num2 = 1;

            System.out.println("J");

            while (num2 <= 3) {
                System.out.println("D");
                num2++;
            }

            num++;
        }
    }
}
